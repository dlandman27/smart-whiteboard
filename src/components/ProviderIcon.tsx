/**
 * Renders the correct branded icon for a given provider.
 * Use this everywhere instead of ad-hoc icon logic.
 *
 *   <ProviderIcon provider="gcal" size={14} />
 */
export function ProviderIcon({
  provider,
  size = 16,
  color,
  style,
}: {
  provider: string
  size?: number
  color?: string
  style?: React.CSSProperties
}) {
  const merged: React.CSSProperties = {
    display: 'inline-flex',
    ...style,
    ...(color ? { color } : undefined),
  }

  if (provider === 'builtin') return <span style={merged}><Logo size={size} /></span>

  const brand = BRANDS[provider]
  if (brand) return <span style={merged}><BrandSvg size={size} path={brand.path} hex={brand.hex} /></span>

  return <Icon icon="CircleDashed" size={size} style={merged} />
}
